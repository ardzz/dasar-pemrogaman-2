def add(bilangan_pertama: int or float, bilangan_kedua: int or float) -> int or float:
    return bilangan_pertama + bilangan_kedua


def subtract(bilangan_pertama: int or float, bilangan_kedua: int or float) -> int or float:
    return bilangan_pertama - bilangan_kedua


def multiple(bilangan_pertama: int or float, bilangan_kedua: int or float) -> int or float:
    return bilangan_pertama * bilangan_kedua


def divide(bilangan_pertama: int or float, bilangan_kedua: int or float) -> int or float:
    return bilangan_pertama / bilangan_kedua


def mod(bilangan_pertama: int or float, bilangan_kedua: int or float) -> int or float:
    return bilangan_pertama / bilangan_kedua


def exponent(bilangan_pertama: int or float, bilangan_kedua: int or float) -> int or float:
    return bilangan_pertama / bilangan_kedua


def floor_divide(bilangan_pertama: int or float, bilangan_kedua: int or float) -> int or float:
    return bilangan_pertama / bilangan_kedua
